// Exercise library for the Young Athlete app.
//
// Philosophy (Joey Bergles / Chad Wesley Smith youth LTAD): for an 11-year-old
// beginner the goal is MOVEMENT COMPETENCY and GENERAL ATHLETICISM, not load.
// Master bodyweight patterns, learn to jump and LAND, learn to sprint, throw,
// brace — a wide base. Weight/intensity come much later. So every exercise
// here leads with form cues and a video, prescriptions are modest, and the
// "level" field gates beginners into the simplest variations first.
//
// Each exercise:
//   key        unique id (stored in logs)
//   name       display name
//   pattern    movement family — see PATTERNS below
//   equipment  none | bands | dumbbell | medball | box | bar
//   level      1 (beginner) .. 3 (more advanced) — start at 1
//   rx         kid-friendly prescription string
//   cues       2-3 short coaching cues (the teaching, in plain language)
//   why        one short line on why it matters (motivation)
//   video      YouTube search URL (rot-proof — see lib/youtube)

